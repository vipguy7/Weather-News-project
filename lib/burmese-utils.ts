// Generate a descriptive Burmese weather message for the day
export function generateDescriptiveBurmeseWeatherMessage(
  city: string,
  condition: WeatherCondition,
  hourlyForecast?: HourlyForecast[],
): string {
  // Get Burmese city name or use original if not available
  const cityBurmese = cityNamesBurmese[city] || city

  // Get random descriptions for different times of day
  const morningDesc = timeOfDayWeatherDescriptions.morning[condition] || timeOfDayWeatherDescriptions.morning.Clear
  const noonDesc = timeOfDayWeatherDescriptions.noon[condition] || timeOfDayWeatherDescriptions.noon.Clear
  const eveningDesc = timeOfDayWeatherDescriptions.evening[condition] || timeOfDayWeatherDescriptions.evening.Clear

  // Get random descriptions from each time period
  const morningText = morningDesc[Math.floor(Math.random() * morningDesc.length)]
  const noonText = noonDesc[Math.floor(Math.random() * noonDesc.length)]
  const eveningText = eveningDesc[Math.floor(Math.random() * eveningDesc.length)]

  // Construct the message
  return `${cityBurmese} မြို့၏ ယနေ့ရာသီဥတု အခြေအနေမှာ - ${morningText}။ ${noonText}။ ${eveningText}။`
}

// Get a random health tip based on weather condition
export function getRandomHealthTip(condition: WeatherCondition): string {
  const tips = healthTips[condition] || healthTips.Clear
  return tips[Math.floor(Math.random() * tips.length)]
}
